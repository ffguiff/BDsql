SELECT DEPTNO,COUNT(DEPTNO) "QUANTIDADE DE FUNCIONARIOS", JOB, SUM(SAL) "SOMA DOS SALARIOS"
FROM SCOTT.EMP
GROUP BY DEPTNO,JOB
ORDER BY DEPTNO,JOB;-- AGRUPA POR DEPARTAMENTO E CARGO E CALCULA A SOMA DOS SALARIOS DE CADA CARGO EM CADA DEPARTAMENTO

SELECT DEPTNO, SUM(SAL)
FROM SCOTT.EMP
GROUP BY DEPTNO
HAVING SUM(SAL)>9000;-- AGRUPA O SALARIOS DOS DEPARTAMENTOS E SOMA OS SEUS VALORES DANDO
-- GASTO TOTAL COM CADA DEPARTAMENTO, O HAVING FILTRA E EXIBE SOMENTE OS DEPARTAMENTOS
-- QUE TEM GASTO COM FUNCIONARIOS ACIMA DE 9000

SELECT ENAME
FROM SCOTT.EMP 
WHERE SAL>
    (SELECT SAL
    FROM SCOTT.EMP
    WHERE EMPNO=7566); -- SUBCONSULTA = EXIBE OS NOMES DOS FUNCIONARIOS QUE RECEBEM MAIS
    -- QUE O FUNCIONARIO 7566

SELECT EMPNO, ENAME, JOB
FROM SCOTT.EMP
    WHERE SAL < ANY 
        (SELECT SAL
        FROM SCOTT.EMP
        WHERE JOB = 'CLERK')
    AND JOB<> 'CLERK'; -- ESTA CONSULTA RETORNA TODOS SALARIOS DO CARGO CLERK POR SER VARIOS
    -- VALORES DENOMINAMOS ESTE TIPO DE CODIGO COM SUBCONSULTAS DE VARIAS LINHAS E COLUNAS
    -- POR ESTE MOTIVO USAMOS A PALAVRA RESERVADA ANY QUE PODE EXIBIR:
    -- SALARIOS MAIORES QUE: 800 OU 1300 OU 1100 OU 950

SELECT EMPNO, ENAME, JOB
FROM SCOTT.EMP
    WHERE SAL < ALL 
        (SELECT AVG(SAL)
        FROM SCOTT.EMP
        GROUP BY DEPTNO); -- ESTA CONSULTA RETORNA TODOS SALARIOS (ALL) MAIORES QUE A MEDIA DE CADA DEPARTAMENTO
        
CREATE OR REPLACE VIEW DEPT10_VU
AS SELECT ENAME,SAL,DEPTNO
FROM SCOTT.EMP
WHERE DEPTNO=10; --CRIAÇÃO DE VIEW SIMPLES

CREATE VIEW DEPT_SUM_VU
(NAME,MINSAL,MAXSAL,AVGSAL)--APELIDOS DAS COLUNAS DA VIEW
AS SELECT D.DNAME,MIN(E.SAL),MAX(E.SAL),AVG(E.SAL)
FROM SCOTT.EMP E JOIN SCOTT.DEPT D
ON (E.DEPTNO = D.DEPTNO)
GROUP BY D.DNAME; -- VIEW COMPLEXA. COMPLEXA POR TER FUNÇÕES DE GRUPO 

CREATE OR RAPLACE VIEW EMPVU20
