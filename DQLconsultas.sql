SELECT SCOTT.EMP.EMPNO, SCOTT.EMP.ENAME, SCOTT.EMP.DEPTNO, SCOTT.DEPT.DEPTNO, SCOTT.DEPT.LOC
FROM SCOTT.EMP JOIN SCOTT.DEPT
ON SCOTT.EMP.DEPTNO = SCOTT.DEPT.DEPTNO;-- MESCLA INFORMAÇÕES DE AMBAS AS 
--TABELAS ONDE ESTIVER O MESMO CODIGO DE DEPARTAMENTO

SELECT E.EMPNO, E.ENAME, E.DEPTNO, D.DEPTNO, D.LOC
FROM SCOTT.EMP E JOIN SCOTT.DEPT D
ON (E.DEPTNO=D.DEPTNO);-- MESMO CODIGO DO DESCRITO ACIMA, MAS COM USO DE 
--APELIDOS PARA AS TABELAS

SELECT E.LAST_NAME "PRIMEIRO NOME", D.DEPARTMENT_NAME "DEPARTAMENTO", L.CITY "CIDADE"
FROM HR.EMPLOYEES E JOIN HR.DEPARTMENTS D
ON (E.DEPARTMENT_ID = D.DEPARTMENT_ID)
JOIN HR.LOCATIONS L 
ON(D.LOCATION_ID = L.LOCATION_ID);-- NO BD HR DO ORACLE USADO PARA FAZER UM JOIN
-- TRIDIMENCIONAL JUNTANDO INFORMAÇÕES DE 3 TABELAS DISTINTAS

SELECT E.LAST_NAME, D.DEPARTMENT_ID, D.DEPARTMENT_NAME
FROM HR.EMPLOYEES E LEFT OUTER JOIN HR.DEPARTMENTS D
ON (E.DEPARTMENT_ID = D.DEPARTMENT_ID)
  ORDER BY E.LAST_NAME; --JOIN EXTERNA

